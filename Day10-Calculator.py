def add(n1, n2):
    return n1 + n2

def subtract(n1, n2):
    return n1 - n2

def multiply(n1, n2):
    return n1 * n2

def divide(n1, n2):
    return n1 / n2

operations = {
    "+" : add,
    "-" : subtract,
    "*" : multiply,
    "/" : divide
}

def calculator():
    print(logo)
    num1 = float(input("Enter the first number : "))
    continue_loop = True
    while continue_loop:
        for i in operations:
            print(i)
        op = input("Enter the operation you want to perform from above : ")
        num2 = float(input("Enter the second number : "))
        result = operations[op](num1, num2)
        print(f"{num1} {op} {num2} = {result}")
        exit = input("Type 'y' continue with the result or 'n' to start fresh or 'e' to exit: ")
        if exit == 'n':
            continue_loop = False
            calculator()
        elif exit == 'e':
            return
        else:
            num1 = result

calculator()